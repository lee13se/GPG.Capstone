</p>


<p><a name="newOrder" /><strong>New Order - Enter Quote Number</strong></p>

<p>All sales orders are created from an associated quote.If no quote was issued,a new quote is created first.<br></br> 
   Entering the quote number will retrieve the customer and associated quote data. It will display upon selecting the
   <strong><em>Get Quote Info</em></strong> button or using the <i> Enter</i> key. <br></br>
	
</p>

