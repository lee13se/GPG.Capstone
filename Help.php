   Shipping detail (this will calculate the total sales order quantity)<br>
   Select Shipping Address</Font Color="FF0000"> <br></br>

   If a new shipping address is needed in can be created by selecting the 
   <strong><em>New Shipping Address</em></strong> button. <br>
   Select the <strong><em>Submit New Order</em></strong> button to complete the order.
