<h3> Inventory </h3>

<p><a name="RawInvList" /><strong>Raw Material Inventory List</strong></p>


<p>This view shows the current raw material inventories. Select <Font Color="FF0000"><u>Update Inventory</u></Font Color="FF0000"> 
   or <Font Color="FF0000"><u>Edit</u></Font Color="FF0000"> to make changes.<br>

   To add a new raw material select <Font Color="FF0000"><u>Add a New Material</u></Font Color="FF0000">. 

</p>

  
<p><a name="UpRawMat" /><strong>Raw Material - Update Inventory</strong></p>


<p><Font Color="FF0000">To update the raw material inventory, the selection has to be in the following order: 
    material, grade, diameter. <br></br></Font Color="FF0000"> 
  
   Select <u>material</u> from the drop box. Next select the <u>grade</u> and the <u>diameter</u>.<br>
   
   To <u>increase</u> the raw material enter the length as a <u>positive</u> number.<br></br>

   To <u>decrease</u> the raw material enter the length as a <u>negative</u> number. <br></br>

   To save the updates select the <strong><em> Update</em></strong> button. 
   
</p>


<p><a name="NewRawMat" /><strong>Raw Material - New Material</strong></p>

<p>Select a material from the dropdown menu or enter a new material in the <strong><em> New </em></strong>field.
   Enter the grade and diameter.These are required fields. The cost field is optional, but provides a default value for quotes if entered. <br></br>

   To complete the addition of the material select the <strong><em> Save </em></strong>button.

</p>



<p><a name="FGInvList" /><strong>Finished Goods Inventory List</strong></p>

<p>This view shows the current finished goods inventories. Select <Font Color="FF0000"><u>Update Inventory</u></Font Color="FF0000"> 
   or <Font Color="FF0000"><u>Edit</u></Font Color="FF0000"> to make changes.<br>

   To add a new part to the finished good inventory 
   select <Font Color="FF0000"><u>Add a New Material</u></Font Color="FF0000">. 

</p>



<p><a name="updateFGInvList" /><strong>Finished Goods Inventory - Update Inventory</strong></p>

<p>Enter the <u>part number</u>. Available part numbers will display as entry is made. <br></br>
   
   To <u>increase</u> the finished goods inventory enter the quantity as a <u>positive</u> number.<br></br>

   To <u>decrease</u> the finished goods inventory enter the quantity as a <u>negative</u> number. <br></br>

   To save the updates select the <strong><em> Update</em></strong> button. 
   



