<h3> Quote & Order </h3>

<p><a name="newQuote" /><strong>New Quote - Enter Client</strong>
  
</p>
<p>Enter the client name or client ID.<br> 
</br> Existing client names will display based on entry.
      If no corresponding client exists, select the <strong><em> New Client </em></strong> 
      button and create a new client. 
</p>

<p><a name="ClientInfo" /><strong>New Quote - Client Information</strong></p>

<p>Verify client information. If correct, select the<strong><em> Next: Part Number </em></strong>button  
   to proceed to the next step in the quote process. <br> <br>
   If the client information needs to be updated select the <strong><em>Edit Client</em></strong> button on the bottom of   
   the screen. <br> <br>	
   If it is determined that the displayed client is not correct,a new client can be created by selecting the 	
   <strong><em>New Client</em></strong> button.


<p><a name="newQuotePart" /><strong>New Quote - Enter Part Number</strong></p>
  
<p>Enter the part number.<br> 
   </br> Existing part numbers will display based on numbers entered. If no corresponding part exists select the <strong><em> Create a New Part </em></strong> 
   button and create a new part. <br></br>

   Enter the raw material as a <u>price per unit.</u> <br></br>

   Enter the quote quantity requested. If multiple quantities are requested select the <strong><em> Add Row </em></strong> button for additional fields. 

</p>


<p><a name="newQuoteSum" /><strong>New Quote - Quote Summary</strong></p>

<p>There are no entries made on this screen. This displays the completed quote.<br></br>
   To print the quote: Select the print button on the browser's <u>file menu.</u></p>



<p><a name="existingQuote" /><strong>Existing Quote - Enter Quote Number</strong></p>

<p>Entering the quote number will retrieve the customer and associated quote data.
   Press the enter key or select the <strong><em>Submit</em></strong> button. <br></br>
  
<p>Selecting the <strong><em>Create Sales Order From Quote</em></strong>button moves the process to 
   the sales order entry screen.<br></br>

   Selecting the client will display a list of open quotes for the client. 	
</p>


<p><a name="currentQuote" /><strong>Existing Quote - Current Quotes</strong></p>

<p>The view shows existing quotes for the customer, which are <u>still valid.</u> <br></br>

   To select a specific quote, click on the quote number, which will open the selected quote.

</p>   	
 
<p><a name="currentQuote1" /><strong>Existing Quote - All Quotes</strong></p>

<p>The view shows all quotes for the customer, <u>regardless of validity</U>. <br></br>

